#   cria uma lista com valores determinados
lista = [1, 2, 54, 8, 9, 7]

#   exibe o maior numero da lista
print(max(lista))
#   exibe o menor numero da lista
print(min(lista))

#   ordena a lista asc
lista.sort()
print(lista)

#   adiociona no final da lista o valor
lista.append(55)
print(lista)


# insere na posição desejada pelo index
lista.insert(0, 22)
print(lista)


#   remove por index
lista.pop(3)
print(lista)

# remove por valor
lista.remove(2)
print(lista)

#   limpa toda a lista
lista.clear()
print(lista)


