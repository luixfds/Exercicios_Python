#   Formatação

#   variavel
nome = "Ola mundo"

# retorna o tamanho da variável
print(len(nome))

#   adiciona @ (ou outro valor) caso a quantidade dentro da variavel seja menor que o numero 10
print(nome.rjust(10, "@"))

#   coloca em letra maiuscula o texto da variavel
print(nome.upper())

#   coloca em letra minuscula o texto da variavel
print(nome.lower())

#   formata em titulo o texto da variavel
print(nome.title())

